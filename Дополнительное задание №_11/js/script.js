let number = Number(prompt("Загадайте любое число:"));
let doubled = number * 2;
alert(`Удвоенное число: ${doubled}`);
let plusTen = doubled + 10;
alert(`После прибавления 10: ${plusTen}`);
let halved = plusTen / 2;
alert(`После деления на 2: ${halved}`);
let finalResult = halved - number;
alert(`Результат вычитания исходного числа: ${finalResult}`);
alert("Ответ всегда равен 5!");

